int cvi_board_init(void)
{
	PINMUX_CONFIG(CAM_MCLK0, CAM_MCLK0);

	PINMUX_CONFIG(IIC3_SCL, IIC3_SCL);
	PINMUX_CONFIG(IIC3_SDA, IIC3_SDA);

	/*Network indicator light*/
	PINMUX_CONFIG(PWR_WAKEUP0, EPHY_LNK_LED);
	PINMUX_CONFIG(PWR_GPIO2, EPHY_SPD_LED);

	/*HOST_PWR_EN*/
	PINMUX_CONFIG(IIC2_SDA, PWR_GPIO_13);

	/*SYS_LED*/
	PINMUX_CONFIG(VIVO_D10, XGPIOB_11);

	/*USR_KEY*/
	PINMUX_CONFIG(USB_ID, XGPIOB_4);

	/*PWR_KEY*/
	PINMUX_CONFIG(PWR_BUTTON1, PWR_GPIO_8);

	/*mmio_write_32(0x050250AC,0X2);
	mmio_write_32(0x05027084,0x11);
	mmio_write_32(0x3001098,0x0);
	mmio_write_32(0x05027084,0x0);
	mmio_write_32(0x050260BC,0x1000); */

	/*UART1*/
	PINMUX_CONFIG(IIC0_SCL, UART1_TX);
	PINMUX_CONFIG(IIC0_SDA, UART1_RX);

	/*WIFI*/
	pinmux_config(PINMUX_SDIO1);
	PINMUX_CONFIG(CAM_PD1, XGPIOA_4);
	PINMUX_CONFIG(CAM_MCLK0, XGPIOA_0);

	mmio_write_32(0x03020004, mmio_read_32(0x03020004) | 0x40000000);
	mmio_write_32(0x03020000, mmio_read_32(0x03020000) | 0x40000000);

	PINMUX_CONFIG(UART2_RX, UART2_RX);
	PINMUX_CONFIG(UART2_TX, UART2_TX);
	PINMUX_CONFIG(UART2_RTS, UART2_RTS);
	PINMUX_CONFIG(UART2_CTS, UART2_CTS);

	/*sensor I2C*/
	PINMUX_CONFIG(VIVO_D4, IIC1_SDA);
	PINMUX_CONFIG(VIVO_D3, IIC1_SCL);

	PINMUX_CONFIG(VIVO_D7, IIC2_SDA);
	PINMUX_CONFIG(VIVO_D8, IIC2_SCL);

	/*Sensor PWDN PIN*/
	PINMUX_CONFIG(AUX0, XGPIOA_30);
	PINMUX_CONFIG(JTAG_CPU_TMS, XGPIOA_19);

	mmio_write_32(0x03020004, mmio_read_32(0x03020004) | 0x2);
	mmio_write_32(0x03020000, mmio_read_32(0x03020000) | 0x2);

	mmio_write_32(0x03020004, mmio_read_32(0x03020004) | 0x10);
	mmio_write_32(0x03020000, mmio_read_32(0x03020000) | 0x10);
	
	/*Sensor Reset PIN*/
	PINMUX_CONFIG(JTAG_CPU_TRST, XGPIOA_20);
	PINMUX_CONFIG(JTAG_CPU_TCK, XGPIOA_18);

	/*touch*/
	PINMUX_CONFIG(VIVO_D0, IIC4_SDA);
	PINMUX_CONFIG(VIVO_D1, IIC4_SCL);
	PINMUX_CONFIG(VIVO_D9, XGPIOB_12);
	PINMUX_CONFIG(VIVO_CLK, XGPIOB_22);

	/*MIPI_PWR_EN*/
	PINMUX_CONFIG(IIC2_SCL, PWR_GPIO_12);

	/*MIPI_RST*/
	PINMUX_CONFIG(VIVO_D6, XGPIOB_15);

	pinmux_config(PINMUX_DSI);

	return 0;
}
